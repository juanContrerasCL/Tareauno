package com.prspringjv.prspringjvprueba2.domain.dto;

public class Location {
    private Integer localidadId;
    private String descripcionId;
    private String poligonoId;
    private String colorId;

    public Integer getLocalidadId() {
        return localidadId;
    }

    public void setLocalidadId(Integer localidadId) {
        this.localidadId = localidadId;
    }

    public String getDescripcionId() {
        return descripcionId;
    }

    public void setDescripcionId(String descripcionId) {
        this.descripcionId = descripcionId;
    }

    public String getPoligonoId() {
        return poligonoId;
    }

    public void setPoligonoId(String poligonoId) {
        this.poligonoId = poligonoId;
    }

    public String getColorId() {
        return colorId;
    }

    public void setColorId(String colorId) {
        this.colorId = colorId;
    }
}
